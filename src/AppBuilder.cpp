std::vector<GeneratedFile> AppBuilder::GeneratePython(const AppBuilderConfig& config) const
{
    std::string requirement;
    if (config.framework == "CustomTkinter") requirement = "customtkinter>=5.2.2\n";
    if (config.framework == "PyQt6") requirement = "PyQt6>=6.7.0\n";
    if (config.framework == "Dear PyGui") requirement = "dearpygui>=1.11.1\n";
    if (config.framework == "Streamlit") requirement = "streamlit>=1.36.0\n";
    if (config.framework == "Flet") requirement = "flet>=0.23.0\n";
    requirement += "pyinstaller>=6.8.0\nnuitka>=2.3.0\n";

    std::string main = ReplaceAll(PythonMain(config), "{{APP_NAME}}", config.appName);
    main = LicensePrefix(config, "# ") + main;

    std::string pyproject = ReplaceAll(R"TOML([project]
name = "{{SLUG}}"
version = "{{VERSION}}"
requires-python = ">=3.11"
dependencies = []

[tool.setuptools]
py-modules = ["main"]
)TOML", "{{SLUG}}", ProjectSlug(config.appName));
    pyproject = ReplaceAll(pyproject, "{{VERSION}}", config.version);

    std::string setup = ReplaceAll(R"PY(from setuptools import setup

setup(
    name="{{SLUG}}",
    version="{{VERSION}}",
    py_modules=["main"],
)
)PY", "{{SLUG}}", ProjectSlug(config.appName));
    setup = ReplaceAll(setup, "{{VERSION}}", config.version);

    return {
        { "main.py", main },
        { "requirements.txt", requirement },
        { "pyproject.toml", pyproject },
        { "setup.py", setup },
        { "scripts/setup_venv.bat", "python -m venv .venv\r\ncall .venv\\Scripts\\activate\r\npython -m pip install --upgrade pip\r\npip install -r requirements.txt\r\n" },
        { "scripts/build_pyinstaller.bat", "call .venv\\Scripts\\activate\r\npyinstaller --noconfirm --onefile --windowed main.py\r\n" },
        { "scripts/build_nuitka.bat", "call .venv\\Scripts\\activate\r\npython -m nuitka --standalone --onefile --windows-console-mode=disable main.py\r\n" },
        { "README.md", CommonReadme(config, PythonCommands(config)) }
    };
}

