std::string AppBuilder::ProjectSlug(const std::string& appName) const
{
    std::string slug;
    bool previousDash = false;
    for (unsigned char character : appName) {
        if (std::isalnum(character)) {
            slug.push_back(static_cast<char>(std::tolower(character)));
            previousDash = false;
        } else if (!previousDash && !slug.empty()) {
            slug.push_back('-');
            previousDash = true;
        }
    }
    while (!slug.empty() && slug.back() == '-') {
        slug.pop_back();
    }
    return slug;
}

std::string AppBuilder::ClassName(const std::string& appName) const
{
    std::string name;
    bool capitalize = true;
    for (unsigned char character : appName) {
        if (std::isalnum(character)) {
            name.push_back(static_cast<char>(capitalize ? std::toupper(character) : character));
            capitalize = false;
        } else {
            capitalize = true;
        }
    }
    if (name.empty() || std::isdigit(static_cast<unsigned char>(name.front()))) {
        name.insert(name.begin(), 'A');
    }
    return name;
}

std::string AppBuilder::LicensePrefix(const AppBuilderConfig& config, const std::string& commentPrefix) const
{
    if (!config.features.includeLicenseHeader || config.licenseHeader.empty()) {
        return {};
    }

    std::ostringstream output;
    std::istringstream input(config.licenseHeader);
    std::string line;
    while (std::getline(input, line)) {
        output << commentPrefix << line << "\n";
    }
    output << "\n";
    return output.str();
}

bool AppBuilder::WriteProject(const AppBuilderConfig& config, const std::vector<GeneratedFile>& files, std::filesystem::path& projectDirectory, std::string& error) const
{
    projectDirectory = config.outputDirectory / ProjectSlug(config.appName);
    std::error_code code;
    std::filesystem::create_directories(projectDirectory, code);
    if (code) {
        error = code.message();
        return false;
    }

    for (const GeneratedFile& file : files) {
        const std::filesystem::path target = projectDirectory / file.relativePath;
        std::filesystem::create_directories(target.parent_path(), code);
        if (code) {
            error = code.message();
            return false;
        }

        std::ofstream stream(target, std::ios::binary);
        if (!stream) {
            error = "Unable to write " + target.string();
            return false;
        }
        stream << file.contents;
    }

    return true;
}

bool AppBuilder::CreateZip(const std::filesystem::path& projectDirectory, std::filesystem::path& zipPath, std::string& error) const
{
    zipPath = projectDirectory;
    zipPath += ".zip";
    const std::string command = "powershell -NoProfile -ExecutionPolicy Bypass -Command \"if (Test-Path '" + zipPath.string() + "') { Remove-Item '" + zipPath.string() + "' -Force }; Compress-Archive -Path '" + projectDirectory.string() + "\\*' -DestinationPath '" + zipPath.string() + "'\"";
    const int exitCode = std::system(command.c_str());
    if (exitCode != 0) {
        error = "Compress-Archive exited with code " + std::to_string(exitCode);
        return false;
    }
    return true;
}

bool AppBuilder::RunCommand(const std::string& command, const std::filesystem::path& workingDirectory) const
{
    const std::filesystem::path original = std::filesystem::current_path();
    std::error_code code;
    std::filesystem::current_path(workingDirectory, code);
    if (code) {
        return false;
    }
    const int exitCode = std::system(command.c_str());
    std::filesystem::current_path(original, code);
    return exitCode == 0;
}
