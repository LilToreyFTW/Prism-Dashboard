AppBuilderResult AppBuilder::GenerateProject(const AppBuilderConfig& config) const
{
    AppBuilderResult result;
    result.message = Validate(config);
    if (!result.message.empty()) {
        return result;
    }

    result.files = PreviewFiles(config);
    result.commands = BuildCommands(config);

    std::string error;
    if (!WriteProject(config, result.files, result.projectDirectory, error)) {
        result.message = error;
        return result;
    }

    if (config.features.initializeGit) {
        RunCommand("git init", result.projectDirectory);
    }

    if (config.features.createZipArchive && !CreateZip(result.projectDirectory, result.zipPath, error)) {
        result.message = "Project generated, but zip creation failed: " + error;
        return result;
    }

    result.success = true;
    result.message = "Project generated successfully.";
    return result;
}

std::vector<GeneratedFile> AppBuilder::PreviewFiles(const AppBuilderConfig& config) const
{
    switch (config.language) {
    case BuilderLanguage::Python:
        return GeneratePython(config);
    case BuilderLanguage::Cpp:
        return GenerateCpp(config);
    case BuilderLanguage::CSharp:
        return GenerateCSharp(config);
    case BuilderLanguage::Java:
        return GenerateJava(config);
    }
    return {};
}

std::vector<BuildCommand> AppBuilder::BuildCommands(const AppBuilderConfig& config) const
{
    switch (config.language) {
    case BuilderLanguage::Python:
        return PythonCommands(config);
    case BuilderLanguage::Cpp:
        return CppCommands(config);
    case BuilderLanguage::CSharp:
        return CSharpCommands(config);
    case BuilderLanguage::Java:
        return JavaCommands(config);
    }
    return {};
}

std::vector<std::string> AppBuilder::FrameworksFor(BuilderLanguage language) const
{
    switch (language) {
    case BuilderLanguage::Python:
        return { "Tkinter", "CustomTkinter", "PyQt6", "Dear PyGui", "Streamlit", "Flet" };
    case BuilderLanguage::Cpp:
        return { "Dear ImGui + GLFW + OpenGL", "Qt6", "wxWidgets" };
    case BuilderLanguage::CSharp:
        return { "WinForms", "WPF", "MAUI", "AvaloniaUI" };
    case BuilderLanguage::Java:
        return { "Swing", "JavaFX", "TornadoFX" };
    }
    return {};
}

std::string AppBuilder::Validate(const AppBuilderConfig& config) const
{
    if (ProjectSlug(config.appName).empty()) {
        return "App name must contain at least one letter or number.";
    }

    const std::vector<std::string> frameworks = FrameworksFor(config.language);
    if (std::find(frameworks.begin(), frameworks.end(), config.framework) == frameworks.end()) {
        return "Unsupported framework for selected language: " + config.framework;
    }

    if (config.outputDirectory.empty()) {
        return "Output directory is required.";
    }

    return {};
}

