std::string ReplaceAll(std::string value, const std::string& from, const std::string& to)
{
    size_t position = 0;
    while ((position = value.find(from, position)) != std::string::npos) {
        value.replace(position, from.length(), to);
        position += to.length();
    }
    return value;
}

std::string JoinCommands(const std::vector<BuildCommand>& commands)
{
    std::ostringstream output;
    for (const BuildCommand& command : commands) {
        output << "- " << command.label << ": `" << command.command << "`\n";
    }
    return output.str();
}

std::string CommonReadme(const AppBuilderConfig& config, const std::vector<BuildCommand>& commands)
{
    std::ostringstream readme;
    readme << "# " << config.appName << "\n\n";
    readme << "Generated by PrismDashboard App Builder.\n\n";
    readme << "## Project\n";
    readme << "- Version: " << config.version << "\n";
    readme << "- Framework: " << config.framework << "\n";
    readme << "- Theme: " << config.theme << "\n\n";
    readme << "## Commands\n" << JoinCommands(commands) << "\n";
    readme << "## Notes\n";
    readme << "The generated files are complete starter applications with install, run, and packaging scripts.\n";
    return readme.str();
}

